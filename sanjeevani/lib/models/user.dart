class User {
  final String name;
  final String age;
  final String city;
  final String phone;
  final String email;
  final String password;
  final String gender;

  User({
    required this.name,
    required this.age,
    required this.city,
    required this.phone,
    required this.email,
    required this.password,
    required this.gender,
  });
}